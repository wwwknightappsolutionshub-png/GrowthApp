@router.get("/models")
async def get_social_models(
    _: SuperAdmin,
    db: AsyncSession = Depends(get_db),
) -> dict[str, Any]:
    """Return counts across all AI Social tables."""
    brand_count = (await db.execute(select(func.count()).select_from(SocialBrandIdentity))).scalar_one()
    draft_count = (await db.execute(select(func.count()).select_from(SocialContentDraft))).scalar_one()
    upload_count = (await db.execute(select(func.count()).select_from(SocialSampleUploads))).scalar_one()
    prefs_count = (await db.execute(select(func.count()).select_from(SocialPostingPreferences))).scalar_one()
    approval_count = (await db.execute(select(func.count()).select_from(SocialApprovalQueue))).scalar_one()
    schedule_count = (await db.execute(select(func.count()).select_from(SocialScheduleQueue))).scalar_one()

    return {
        "models": {
            "social_brand_identities": brand_count,
            "social_content_drafts": draft_count,
            "social_sample_uploads": upload_count,
            "social_posting_preferences": prefs_count,
            "social_approval_queue": approval_count,
            "social_schedule_queue": schedule_count,
        }
    }


@router.post("/ai-config")
async def set_ai_config(
    body: AiConfigIn,
    _: SuperAdmin,
) -> dict[str, Any]:
    """Update the global AI Social generation config."""
    if body.model is not None:
        _ai_config["model"] = body.model
    if body.max_drafts_per_run is not None:
        _ai_config["max_drafts_per_run"] = body.max_drafts_per_run
    if body.auto_approve is not None:
        _ai_config["auto_approve"] = body.auto_approve
    if body.default_tone is not None:
        _ai_config["default_tone"] = body.default_tone
    if body.enabled_platforms is not None:
        _ai_config["enabled_platforms"] = body.enabled_platforms
    return {"ok": True, "config": _ai_config}


@router.post("/force-generate")
async def force_generate(
    body: ForceGenerateIn,
    _: SuperAdmin,
    db: AsyncSession = Depends(get_db),
) -> dict[str, Any]:
    """Force-generate drafts for a specific tenant (admin override)."""
    import uuid as _uuid
    from app.modules.social.models import SocialContentDraft

    drafts = []
    for i in range(body.count):
        topic = (body.topic_hints[i] if body.topic_hints and i < len(body.topic_hints) else None)
        draft = SocialContentDraft(
            id=_uuid.uuid4(),
            tenant_id=_uuid.UUID(body.tenant_id),
            text_content=f"[Admin generated] Draft {i + 1}" + (f" about {topic}" if topic else ""),
            ai_notes=f"Force-generated by admin. Model: {_ai_config['model']}",
            status="PENDING",
        )
        db.add(draft)
        drafts.append(str(draft.id))

    await db.commit()
    return {"ok": True, "tenant_id": body.tenant_id, "draft_ids": drafts}


@router.get("/tenant-overview")
async def tenant_overview(
    _: SuperAdmin,
    db: AsyncSession = Depends(get_db),
) -> dict[str, Any]:
    """Overview of all tenants' AI Social usage."""
    drafts_by_status = (
        await db.execute(
            select(SocialContentDraft.status, func.count().label("count"))
            .group_by(SocialContentDraft.status)
        )
    ).all()

    pending_approvals = (
        await db.execute(
            select(func.count()).select_from(SocialApprovalQueue).where(
                SocialApprovalQueue.response_received == False  # noqa: E712
            )
        )
    ).scalar_one()

    scheduled_posts = (
        await db.execute(
            select(func.count()).select_from(SocialScheduleQueue).where(
                SocialScheduleQueue.posted_status == "SCHEDULED"
            )
        )
    ).scalar_one()

    published_posts = (
        await db.execute(
            select(func.count()).select_from(SocialScheduleQueue).where(
                SocialScheduleQueue.posted_status == "PUBLISHED"
            )
        )
    ).scalar_one()

    return {
        "drafts_by_status": {row.status: row.count for row in drafts_by_status},
        "pending_approvals": pending_approvals,
        "scheduled_posts": scheduled_posts,
        "published_posts": published_posts,
        "ai_config": _ai_config,
    }


