router = APIRouter(prefix="/api/admin/social", tags=["Admin — AI Social"])

# In-memory AI config (extend to DB-backed in production)
_ai_config: dict[str, Any] = {
    "model": "gpt-4o",
    "max_drafts_per_run": 5,
    "auto_approve": False,
    "default_tone": "professional",
    "enabled_platforms": ["FB", "IG", "TIKTOK", "TWITTER"],
}


