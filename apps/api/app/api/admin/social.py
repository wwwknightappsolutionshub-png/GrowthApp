@router.get("/tenants-list")
async def tenants_with_social_activity(
    _: SuperAdmin,
    db: AsyncSession = Depends(get_db),
) -> list[dict[str, Any]]:
    """Tenants list for the manual-regeneration picker, with per-tenant draft counts."""
    rows = (
        await db.execute(
            select(
                Tenant.id,
                Tenant.name,
                func.count(SocialContentDraft.id).label("draft_count"),
            )
            .outerjoin(SocialContentDraft, SocialContentDraft.tenant_id == Tenant.id)
            .group_by(Tenant.id, Tenant.name)
            .order_by(Tenant.name)
        )
    ).all()
    return [
        {"id": str(r.id), "name": r.name, "draft_count": r.draft_count}
        for r in rows
    ]


@router.get("/scheduler")
async def scheduler_status(
    _: SuperAdmin,
    db: AsyncSession = Depends(get_db),
    limit: int = Query(100, le=500),
) -> dict[str, Any]:
    """Scheduler queue status — counts by status + the next 100 scheduled items."""
    counts = (
        await db.execute(
            select(SocialScheduleQueue.posted_status, func.count().label("count"))
            .group_by(SocialScheduleQueue.posted_status)
        )
    ).all()

    now = datetime.now(timezone.utc)
    overdue = (
        await db.execute(
            select(func.count())
            .select_from(SocialScheduleQueue)
            .where(
                SocialScheduleQueue.posted_status == "SCHEDULED",
                SocialScheduleQueue.scheduled_time != None,  # noqa: E711
                SocialScheduleQueue.scheduled_time <= now,
            )
        )
    ).scalar_one()

    items = (
        await db.execute(
            select(
                SocialScheduleQueue.id,
                SocialScheduleQueue.tenant_id,
                SocialScheduleQueue.draft_id,
                SocialScheduleQueue.platform,
                SocialScheduleQueue.scheduled_time,
                SocialScheduleQueue.posted_status,
                Tenant.name.label("tenant_name"),
            )
            .outerjoin(Tenant, Tenant.id == SocialScheduleQueue.tenant_id)
            .order_by(SocialScheduleQueue.scheduled_time.asc().nullsfirst())
            .limit(limit)
        )
    ).all()

    return {
        "counts_by_status": {row.posted_status or "UNKNOWN": row.count for row in counts},
        "overdue": overdue,
        "items": [
            {
                "id": str(r.id),
                "tenant_id": str(r.tenant_id),
                "tenant_name": r.tenant_name,
                "draft_id": str(r.draft_id),
                "platform": r.platform,
                "scheduled_time": r.scheduled_time.isoformat() if r.scheduled_time else None,
                "posted_status": r.posted_status,
            }
            for r in items
        ],
    }


@router.get("/failures")
async def failure_logs(
    _: SuperAdmin,
    db: AsyncSession = Depends(get_db),
    limit: int = Query(200, le=500),
) -> dict[str, Any]:
    """List all failed schedule entries + drafts stuck in REVISE.

    Failures include:
      * SocialScheduleQueue with posted_status='ERROR'
      * SocialContentDraft with status='REVISE'
    """
    failed_schedule = (
        await db.execute(
            select(
                SocialScheduleQueue.id,
                SocialScheduleQueue.tenant_id,
                SocialScheduleQueue.draft_id,
                SocialScheduleQueue.platform,
                SocialScheduleQueue.scheduled_time,
                Tenant.name.label("tenant_name"),
            )
            .outerjoin(Tenant, Tenant.id == SocialScheduleQueue.tenant_id)
            .where(SocialScheduleQueue.posted_status == "ERROR")
            .limit(limit)
        )
    ).all()

    revise_drafts = (
        await db.execute(
            select(
                SocialContentDraft.id,
                SocialContentDraft.tenant_id,
                SocialContentDraft.text_content,
                SocialContentDraft.ai_notes,
                SocialContentDraft.created_at,
                Tenant.name.label("tenant_name"),
            )
            .outerjoin(Tenant, Tenant.id == SocialContentDraft.tenant_id)
            .where(SocialContentDraft.status == "REVISE")
            .order_by(SocialContentDraft.created_at.desc())
            .limit(limit)
        )
    ).all()

    return {
        "publish_errors": [
            {
                "id": str(r.id),
                "tenant_id": str(r.tenant_id),
                "tenant_name": r.tenant_name,
                "draft_id": str(r.draft_id),
                "platform": r.platform,
                "scheduled_time": r.scheduled_time.isoformat() if r.scheduled_time else None,
            }
            for r in failed_schedule
        ],
        "revision_requests": [
            {
                "draft_id": str(r.id),
                "tenant_id": str(r.tenant_id),
                "tenant_name": r.tenant_name,
                "text_content": (r.text_content or "")[:240],
                "ai_notes": (r.ai_notes or "")[:240],
                "created_at": r.created_at.isoformat() if r.created_at else None,
            }
            for r in revise_drafts
        ],
    }


@router.get("/insights")
async def tenant_insights(
    _: SuperAdmin,
    db: AsyncSession = Depends(get_db),
) -> dict[str, Any]:
    """Per-tenant insights: drafts/approvals/scheduled/published counts."""
    rows = (
        await db.execute(
            select(
                Tenant.id,
                Tenant.name,
                func.count(SocialContentDraft.id).label("draft_count"),
            )
            .outerjoin(SocialContentDraft, SocialContentDraft.tenant_id == Tenant.id)
            .group_by(Tenant.id, Tenant.name)
        )
    ).all()

    approval_counts_q = (
        await db.execute(
            select(SocialApprovalQueue.tenant_id, func.count().label("count"))
            .group_by(SocialApprovalQueue.tenant_id)
        )
    ).all()
    approval_counts = {str(r.tenant_id): r.count for r in approval_counts_q}

    sched_q = (
        await db.execute(
            select(
                SocialScheduleQueue.tenant_id,
                SocialScheduleQueue.posted_status,
                func.count().label("count"),
            )
            .group_by(SocialScheduleQueue.tenant_id, SocialScheduleQueue.posted_status)
        )
    ).all()
    sched_counts: dict[str, dict[str, int]] = {}
    for r in sched_q:
        sched_counts.setdefault(str(r.tenant_id), {})[r.posted_status or "UNKNOWN"] = r.count

    return {
        "tenants": [
            {
                "id": str(r.id),
                "name": r.name,
                "drafts": r.draft_count,
                "approvals_sent": approval_counts.get(str(r.id), 0),
                "scheduled": sched_counts.get(str(r.id), {}).get("SCHEDULED", 0),
                "published": sched_counts.get(str(r.id), {}).get("PUBLISHED", 0),
                "errors": sched_counts.get(str(r.id), {}).get("ERROR", 0),
            }
            for r in rows
        ]
    }
